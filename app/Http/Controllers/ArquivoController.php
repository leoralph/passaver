    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $arquivo = auth()->user()->arquivos->find($id);

        auth()->user()->diretorioPrivado()->delete("{$arquivo->diretorio}{$arquivo->referencia}");

        $arquivo->delete();
    }
}
